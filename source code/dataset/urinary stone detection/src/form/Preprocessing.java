  private static BufferedImage resizeImage(BufferedImage originalImage, int newWidth, int newHeight) {
        BufferedImage resizedImage = new BufferedImage(newWidth, newHeight, BufferedImage.TYPE_INT_RGB);
        Graphics2D g = resizedImage.createGraphics();
        g.drawImage(originalImage.getScaledInstance(newWidth, newHeight, Image.SCALE_SMOOTH), 0, 0, newWidth, newHeight, null);
        g.dispose();
        return resizedImage;
    }

    private static void resizeAndMoveImage(File sourceImage, String destinationDirectory) throws IOException {
        BufferedImage originalImage = ImageIO.read(sourceImage);
        int newWidth = 200;
        int newHeight = 200;
        BufferedImage resizedImage = resizeImage(originalImage, newWidth, newHeight);

        String fileName = sourceImage.getName();
        int pos = fileName.lastIndexOf(".");
        String fileNameWithoutExtension = pos > 0 ? fileName.substring(0, pos) : fileName;

        File newFile = new File(destinationDirectory + File.separator + fileNameWithoutExtension + ".png");
        ImageIO.write(resizedImage, "png", newFile);

    }
